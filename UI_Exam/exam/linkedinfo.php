&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Linked lists are dynamic data structures where nodes can be added, deleted or updated with a minimal cost. Also linked lists do not require access to a large contiguous block of memory at compile time, but rather accessing memory as needed during runtime. 
<br>This flexibility makes linked lists attractive data structures for many practical applications.<br>
 
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; some of the basic operations on linked lists such as adding, deleting and updating node elements. In an array, adding or deleting an entry requires shifting of the array to “fill” or “create” holes.
Updating an entry in an array only requires accessing entry by its index and changing its value.<br><br>
<b>BASIC OPERATIONS</b><br><br>
Some of the basic operations that can be performed on a linked list are<br><br>

1. Traversing a linked list.<br>
2. Appending a new node (to the end) of the list<br>
3. Prepending a new node (to the beginning) of the list<br>
4. Inserting a new node to a specific position on the list<br>
5. Deleting a node from the list<br>
6. Updating the data of a linked list node<br><br>
 
<b>Traversing a Linked List</b><br><br>
A  linked list is a linear data structure that needs to be traversed starting from the head node until the end of the list.<br><br>
a. Start with the head of the list. Access the content of the head node if it is not null.<br>
b. Then go to the next node(if exists) and access the node information<br>
c. Continue until no more nodes (that is, you have reached the last node)<br><br>

 

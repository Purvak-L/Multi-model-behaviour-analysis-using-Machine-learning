 <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;An array is a collection of data that holds fixed number of values of same type. For example: if you want to store marks of 100 students, you can create an array for it.
The size and type of arrays cannot be changed after its declaration.</p>

<b>Arrays are of two types:</b><br><br>

One-dimensional arrays<br>
Multidimensional arrays<br><br>

<b>How to declare an array in C?</b><br><br>
For example,

float mark[5];<br>

Few key notes:<br>
<ol>
<li>Arrays have 0 as the first index not 1. In this example, mark[0]</li>
<li>If the size of an array is n, to access the last element, (n-1) index is used. In this example, mark[4]</li>
<li>Suppose the starting address of mark[0] is 2120d. Then, the next address, a[1], will be 2124d, address of a[2] will be 2128d and so on. It's because the size of a float is 4 bytes.<br><br><br>
</ol>

